package Strivers_A2Z_DSA_Course;

import java.util.ArrayList;

public class forLoop {
    public static void main(String[] args) {
        int  n , count = 6;
        ArrayList<Integer> al = new ArrayList<>();
        al.add(0);
        al.add(1);
        for (int i = 2; i < count; ++i) {
            n  = 0+1;
            al.add(n);
        }
    }
}
